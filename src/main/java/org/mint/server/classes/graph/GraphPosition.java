package org.mint.server.classes.graph;

public class GraphPosition {
  float x;
  float y;
  
  public GraphPosition() {}
  
  public float getX() {
    return x;
  }
  public void setX(float x) {
    this.x = x;
  }
  public float getY() {
    return y;
  }
  public void setY(float y) {
    this.y = y;
  }
  
}
